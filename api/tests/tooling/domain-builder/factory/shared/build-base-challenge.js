import { BaseChallenge, STATUSES, TYPES } from '../../../../../src/shared/domain/models/BaseChallenge.js';

export const buildBaseChallenge = function ({
  id = 'foo id',
  instruction = 'foo instruction',
  alternativeInstruction = 'foo alternativeInstruction',
  proposals = 'foo proposals',
  type = TYPES.QCM,
  solution = 'foo solution',
  solutionToDisplay = 'foo solutionToDisplay',
  t1Status = false,
  t2Status = false,
  t3Status = false,
  status = STATUSES.VALIDATED,
  genealogy = 'foo genealogy',
  blindnessCompatibility = 'foo blindnessCompatibility',
  colorBlindnessCompatibility = 'foo colorBlindnessCompatibility',
  requireGafamWebsiteAccess = false,
  isIncompatibleIpadCertif = false,
  deafAndHardOfHearing = 'foo deafAndHardOfHearing',
  isAwarenessChallenge = false,
  toRephrase = false,
  alternativeVersion = 3,
  shuffled = true,
  illustrationAlt = 'foo illustrationAlt',
  illustrationUrl = 'foo illustrationUrl',
  attachments = ['foo attachment'],
  responsive = 'foo responsive',
  autoReply = false,
  focused = false,
  format = 'foo format',
  timer = 123,
  embedHeight = 456,
  embedUrl = 'foo embedUrl',
  embedTitle = 'foo embedTitle',
  locales = ['foo locale'],
  competenceId = 'foo competenceId',
  skillId = 'foo skillId',
  hasEmbedInternalValidation = false,
  noValidationNeeded = false,
} = {}) {
  return new BaseChallenge({
    id,
    instruction,
    alternativeInstruction,
    proposals,
    type,
    solution,
    solutionToDisplay,
    t1Status,
    t2Status,
    t3Status,
    status,
    genealogy,
    accessibility1: blindnessCompatibility,
    accessibility2: colorBlindnessCompatibility,
    requireGafamWebsiteAccess,
    isIncompatibleIpadCertif,
    deafAndHardOfHearing,
    isAwarenessChallenge,
    toRephrase,
    alternativeVersion,
    shuffled,
    illustrationAlt,
    illustrationUrl,
    attachments,
    responsive,
    autoReply,
    focusable: focused,
    format,
    timer,
    embedHeight,
    embedUrl,
    embedTitle,
    locales,
    competenceId,
    skillId,
    hasEmbedInternalValidation,
    noValidationNeeded,
  });
};

buildBaseChallenge.TYPES = TYPES;
buildBaseChallenge.STATUSES = STATUSES;
